    private void butUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_butUpdateActionPerformed
        lblMessage.setText("");
        if (tableEmployee.getSelectedRow() == -1) {
            if (tableEmployee.getRowCount() == 0) {
                lblMessage.setText("Table is Empty!");
            } else {
                lblMessage.setText("you must select a row!");
            }
        } else {
            DefaultTableModel model = (DefaultTableModel) tableEmployee.getModel();
            model.setValueAt(txtName.getText(), tableEmployee.getSelectedRow(), 0);
            model.setValueAt(comBatch.getSelectedItem().toString(), tableEmployee.getSelectedRow(), 1);
            model.setValueAt(txtAddr.getText(), tableEmployee.getSelectedRow(), 2);
            //model.setValueAt();
        }
    }//GEN-LAST:event_butUpdateActionPerformed

