    private void butAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_butAddActionPerformed
        lblMessage.setText("");
        DefaultTableModel model = (DefaultTableModel) tableEmployee.getModel();
        String rad = null;
        for (Enumeration buttons = butgrpType.getElements(); buttons.hasMoreElements();) {
            AbstractButton button = (AbstractButton) buttons.nextElement();

            if (button.isSelected()) {
                rad = button.getText();
            }
        }
        if (!txtName.getText().trim().equals("")) {
            model.addRow(new Object[]{txtName.getText(), comBatch.getSelectedItem(), txtAddr.getText(), listTech.getSelectedValuesList(), rad});
        } else {
            lblMessage.setText("Employee Name should not be left empty!");
        }
    }//GEN-LAST:event_butAddActionPerformed

    private void butDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_butDeleteActionPerformed
        DefaultTableModel model = (DefaultTableModel) tableEmployee.getModel();
        if (tableEmployee.getSelectedRow() == -1) {
            if (tableEmployee.getRowCount() == 0) {
                lblMessage.setText("Table is empty!");
            } else {
                lblMessage.setText("You must select a row!");
            }
        } else {
            model.removeRow(tableEmployee.getSelectedRow());
        }
    }//GEN-LAST:event_butDeleteActionPerformed

    private void tableEmployeeMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableEmployeeMouseClicked
        DefaultTableModel model = (DefaultTableModel) tableEmployee.getModel();
        txtName.setText(model.getValueAt(tableEmployee.getSelectedRow(), 0).toString());
        comBatch.setSelectedItem(model.getValueAt(tableEmployee.getSelectedRow(), 1).toString());
        txtAddr.setText(model.getValueAt(tableEmployee.getSelectedRow(), 2).toString());
        // butgrpType.setText()
    }//GEN-LAST:event_tableEmployeeMouseClicked

